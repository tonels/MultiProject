package tonels.feature.domain;

public class P3 {
    private Integer id;
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public P3(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

}
